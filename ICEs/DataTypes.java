public class DataTypes {
    public static void main(String[] args) {
        System.out.println("Here are some data types: "); // String (simple output)
        System.out.println("20 + 11"); // String (simple output)
        System.out.println(20 + 11); // int (evaluation)
        System.out.println(17.4 - 3.2); // double (evaluation)
        System.out.println("20" + "4"); // String (concatenation)
        System.out.println(20 + 4); // int (evaluation)
    }
}
