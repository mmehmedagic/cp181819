public class HelloPrinter {
	public static void main(String[] args) {
		System.out.println("Hello"); // displays string
		System.out.print("World"); // displays string
		System.out.println("!"); // displays string
		System.out.print("00"); // displays int
		System.out.print("My lucky number is "); // displays string
		System.out.println(3 +  4 + 5); // displays int (3 + 4 + 5 = 7 + 5 = 12)
	}
}
